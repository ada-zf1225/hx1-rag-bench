#!/usr/bin/env python3
"""Generate the (retriever x dataset) config matrix.

Run from project root:
    python scripts/gen_configs.py            # M4 matrix: Qwen2.5-7B (default)
    python scripts/gen_configs.py --model Qwen/Qwen2.5-72B-Instruct-AWQ \\
                                  --quantization awq \\
                                  --gpu-memory-utilization 0.80
                                                            # M5 matrix

The output is 9 files in `configs/`, one per (retriever, dataset) cell. They
all share model / sampling / eval settings so the only thing that changes
across the matrix is the two named axes — which is what the benchmark table
should reflect.
"""
from __future__ import annotations

import argparse
from pathlib import Path

RETRIEVERS = ("bm25", "bge_m3", "hybrid_rrf")
DATASETS = ("musique", "two_wiki", "hotpotqa")


def _model_short(name: str) -> str:
    """e.g. 'Qwen/Qwen2.5-72B-Instruct-AWQ' -> 'qwen25_72b_awq'."""
    base = name.split("/")[-1].lower().replace("-instruct", "").replace("-", "_")
    return base.replace("qwen2.5", "qwen25")


def _config_yaml(
    *,
    model_name: str,
    quantization: str | None,
    gpu_memory_utilization: float,
    max_tokens: int,
    retriever: str,
    top_k: int,
    dataset: str,
    max_samples: int | None,
) -> str:
    """Build the YAML body line-by-line so optional fields don't disturb indent."""
    lines: list[str] = [
        "# Generated by scripts/gen_configs.py - DO NOT edit by hand.",
        f"# Matrix cell: retriever={retriever}, dataset={dataset}, model={model_name}.",
        "",
        "model:",
        f"  name: {model_name}",
    ]
    if quantization:
        lines.append(f"  quantization: {quantization}")
    lines.extend([
        f"  gpu_memory_utilization: {gpu_memory_utilization}",
        f"  max_tokens: {max_tokens}",
        "  temperature: 0.0",
        "  enable_prefix_caching: true",
        "",
        "retriever:",
        f"  backend: {retriever}",
        f"  top_k: {top_k}",
        "",
        "dataset:",
        f"  name: {dataset}",
        "  split: validation",
        f"  max_samples: {'null' if max_samples is None else max_samples}",
        "  cache_dir: data/raw",
        "",
        "eval:",
        "  metrics: [em, f1, recall_at_k]",
        "",
    ])
    return "\n".join(lines)


def main() -> None:
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument("--model", default="Qwen/Qwen2.5-7B-Instruct")
    p.add_argument("--quantization", default=None,
                   help="vLLM quantization (e.g. 'awq', 'awq_marlin')")
    p.add_argument("--gpu-memory-utilization", type=float, default=0.85)
    p.add_argument("--max-tokens", type=int, default=64)
    p.add_argument("--top-k", type=int, default=5)
    p.add_argument("--max-samples", type=int, default=100,
                   help="Per-config sample cap (use 0 for full validation split)")
    p.add_argument("--out-dir", type=Path, default=Path("configs"))
    args = p.parse_args()

    out_dir = args.out_dir
    out_dir.mkdir(parents=True, exist_ok=True)
    short = _model_short(args.model)
    max_samples = None if args.max_samples == 0 else args.max_samples

    written: list[Path] = []
    for retriever in RETRIEVERS:
        for dataset in DATASETS:
            path = out_dir / f"{short}_{retriever}_{dataset}.yaml"
            path.write_text(
                _config_yaml(
                    model_name=args.model,
                    quantization=args.quantization,
                    gpu_memory_utilization=args.gpu_memory_utilization,
                    max_tokens=args.max_tokens,
                    retriever=retriever,
                    top_k=args.top_k,
                    dataset=dataset,
                    max_samples=max_samples,
                )
            )
            written.append(path)

    print(f"Wrote {len(written)} configs to {out_dir}/:")
    for p in written:
        print(f"  {p}")


if __name__ == "__main__":
    main()
